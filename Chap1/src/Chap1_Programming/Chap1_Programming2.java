package Chap1_Programming;

public class Chap1_Programming2 {

	public static void main(String[] args) {
		// 사칙연산
		System.out.println("덧셈 = " + (1000 + 2000));
		System.out.println("뺄셈 = " + (1000 - 2000));
		System.out.println("곱셈 = " + (1000 * 2000));
		System.out.println("나눗셈 = " + (1000.0 / 2000));
	}

}
